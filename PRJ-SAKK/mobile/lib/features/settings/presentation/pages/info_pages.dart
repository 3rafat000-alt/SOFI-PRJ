/// A reusable section of a document/info page (heading + body).
class _Section {
  final String heading;
  final String body;
  const _Section(this.heading, this.body);
}

class _DocScaffold extends StatelessWidget {
  final String title;
  final String subtitle;
  final IconData icon;
  final String? intro;
  final List<_Section> sections;

  const _DocScaffold({
    required this.title,
    required this.subtitle,
    required this.icon,
    this.intro,
    required this.sections,
  });

  @override
  Widget build(BuildContext context) {
    final colors = context.appColors;
    return AppScaffold(
      title: title,
      subtitle: subtitle,
      body: ListView(
        padding: const EdgeInsets.fromLTRB(
            AppSpacing.xl, AppSpacing.lg, AppSpacing.xl, AppSpacing.xxxl),
        children: [
          Center(
            child: Container(
              width: 76,
              height: 76,
              decoration: BoxDecoration(
                gradient: LinearGradient(
                  colors: colors.cardGradientVisa,
                  begin: Alignment.topLeft,
                  end: Alignment.bottomRight,
                ),
                borderRadius: BorderRadius.circular(22),
              ),
              child: Icon(icon, color: Colors.white, size: 36),
            ),
          ).animate().fadeIn().scale(begin: const Offset(0.85, 0.85)),
          if (intro != null) ...[
            const SizedBox(height: AppSpacing.lg),
            Text(intro!,
                textAlign: TextAlign.center,
                style: TextStyle(
                    fontSize: 13.5,
                    color: colors.textSecondary,
                    height: 1.7)),
          ],
          const SizedBox(height: AppSpacing.xl),
          ...sections.asMap().entries.map((e) => _section(context, e.value)
              .animate(delay: (e.key * 50).ms)
              .fadeIn(duration: 280.ms)
              .slideY(begin: 0.05)),
          const SizedBox(height: AppSpacing.lg),
          Center(
            child: Text('SAKK Wallet · v1.0.0',
                style: TextStyle(fontSize: 12, color: colors.textHint)),
          ),
        ],
      ),
    );
  }

  Widget _section(BuildContext context, _Section s) {
    final colors = context.appColors;
    return Container(
      width: double.infinity,
      margin: const EdgeInsets.only(bottom: AppSpacing.md),
      padding: const EdgeInsets.all(AppSpacing.lg),
      decoration: BoxDecoration(
        color: colors.surface,
        borderRadius: BorderRadius.circular(AppRadius.lg),
        border: Border.all(color: colors.inputBackground),
      ),
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          Row(children: [
            Container(
              width: 6,
              height: 18,
              decoration: BoxDecoration(
                  color: colors.primary,
                  borderRadius: BorderRadius.circular(3)),
            ),
            const SizedBox(width: AppSpacing.sm),
            Expanded(
              child: Text(s.heading,
                  style: TextStyle(
                      fontSize: 15,
                      fontWeight: FontWeight.w700,
                      color: colors.textPrimary)),
            ),
          ]),
          const SizedBox(height: AppSpacing.sm),
          Text(s.body,
              style: TextStyle(
                  fontSize: 13.5,
                  color: colors.textSecondary,
                  height: 1.8)),
        ],
      ),
    );
  }
}

